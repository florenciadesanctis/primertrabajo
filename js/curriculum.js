
window.addEventListener('mouseover',
function(evt){
    if
    (evt.target.classList.contains('color')){
        evt.target.style.color = 'orange';
    }
    });

    window.addEventListener('mouseout',
function(evt){
    if
    (evt.target.classList.contains('color')){
        evt.target.style.color = 'black';
        }
    });


    

